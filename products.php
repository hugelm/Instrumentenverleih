<script type="text/javascript">
$(function(){ 

  $("#pianoBuy").click(function (e) {
    $("#instrument").val($(e.target).text());
    $("#instrumentType").html("Piano");
  });

  $("#drumBuy").click(function (e) {
    $("#instrument").val($(e.target).text());
    $("#instrumentType").html("Schlagzeug");
  });

  $("#guitarBuy").click(function (e) {
    $("#instrument").val($(e.target).text());
    $("#instrumentType").html("Gitarre");
  });

  $(document).on('click','#orderBtn',function(e){

        var valid = this.form.checkValidity();
        if(valid){

            // save form data in variables
            var fName = $('#firstName').val();
            var lName = $('#lastName').val();
            var street = $('#street').val();
            var housenumber = $('#number').val();
            var zipCode = $('#zipCode').val();
            var city = $('#city').val();
            var phone = $('#phone').val();
            var email = $('#email').val();
            var ccHolder = $('#ccHolder').val();
            var ccNumber = $('#ccNumber').val();
            var ccExpire = $('#ccExpire').val();
            var ccCVV = $('#ccCVV').val();
            var instrument = $('#instrument').val();

            e.preventDefault();
            $.ajax({
                type: 'POST',
                url: 'scripts/order.php',
                data: {fName: fName, lName: lName, street: street, housenumber: housenumber, zipCode: zipCode, city: city, phone: phone, email: email,
                ccHolder: ccHolder, ccNumber: ccNumber, ccExpire: ccExpire, ccCVV: ccCVV, instrument: instrument},
                success: function(data){
                    setTimeout('window.location.href = "ordersuccess.php"', 100);
                },
                error: function(data){
                    swal( "Bestellung fehlgeschlagen!" ,  "Bitte wähle dein Lieblingsintrument vor Ort aus." ,  "error" );
                }
            });

        };      
    });

});
